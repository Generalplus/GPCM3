/*---------------------------------------------------------------------------------------
 * Main Function
 *---------------------------------------------------------------------------------------*/
int main()
{
  // GPIO Initialize
  GPIO_SetMode(GPIOA, 0xFFFFFFFF, GPIO_MODE_INPUT);     //IOA[31:0] Input pull-low, bypass IOA[14:13] UART & IOA.8 Output
  MODIFY_REG(GPIOA->OBUF, 0xFFFFFFFF, 0);

  GPIO_SetMode(GPIOB, 0xFFFFFFFF, GPIO_MODE_INPUT);     //IOB Input pull-low
  MODIFY_REG(GPIOB->OBUF, 0xFFFFFFFF, 0);

  GPIO_SetMode(GPIOC, 0xFFFFFFFF, GPIO_MODE_INPUT);     //IOC Input pull-low
  MODIFY_REG(GPIOC->OBUF, 0xFFFFFFFF, 0);

  GPIO_SetMode(GPIOD, 0xFFFFFFFF, GPIO_MODE_INPUT);     //IOD Input pull-low
  MODIFY_REG(GPIOD->OBUF, 0xFFFFFFFF, 0);
  KeyScan_Initial(&KeyScanWorkingRam);                  // Key scan init

  while(1)
  {
    WDT_Clear();
		KeyScan_ServiceLoop();

		ScanedKey = KeyScan_GetCh();
		switch(ScanedKey)
		{
      case 0x01:    // IOA24+VDD: ECB Mode + Verbose
				TestResult = test_encrypt_ecb_verbose();      // Return 1 is correct.
        break;

      case 0x02:    // IOA25+VDD: Encrypt ECB Mode
				TestResult = test_encrypt_ecb();              // Return 1 is correct.
        break;

		  case 0x04:    // IOA26+VDD: Decrypt ECB Mode
        TestResult = test_decrypt_ecb();
        break;

		  case 0x08:    // IOA27+VDD: Encrypt CBC Mode
        TestResult = test_encrypt_cbc();
        break;

		  case 0x10:    // IOA28+VDD: Decrypt CBC Mode
        TestResult = test_decrypt_cbc();
        break;

 		  case 0x20:    // IOA29+VDD: CTR Mode
        TestResult = test_xcrypt_ctr();
        break;
    }
  }
  return 0;
}
