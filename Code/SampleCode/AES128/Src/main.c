static uint8_t CompareResult(uint8_t* in, uint8_t* out, uint16_t size)
{
	uint16_t i;

	for(i=0;i<size;i++)
	{
		if(in[i] != out[i])
		{
			return 0;
		}
	}
	return 1;
}

